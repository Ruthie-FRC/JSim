package jsim.api;

/**
 * Represents the standardized FRC driver station identifiers.
 */
/**
 * Enum representing the standardized FRC driver station identifiers for robots.
 * RED_1, RED_2, RED_3, BLUE_1, BLUE_2, BLUE_3 correspond to alliance and position.
 */
public enum RobotID {
    /** Red alliance, position 1 */
    RED_1,
    /** Red alliance, position 2 */
    RED_2,
    /** Red alliance, position 3 */
    RED_3,
    /** Blue alliance, position 1 */
    BLUE_1,
    /** Blue alliance, position 2 */
    BLUE_2,
    /** Blue alliance, position 3 */
    BLUE_3
}
